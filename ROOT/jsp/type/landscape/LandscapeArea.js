function setSceneryList(obj1ID,obj2ID,ListName)
{
	var objProvince = document.getElementById(obj1ID);
	var objScenery = document.getElementById(obj2ID);
	var i;
	var itemArray = null;
	if(objProvince.value.length > 0)
	{
		itemArray = eval(ListName+objProvince.value);
	}


	for(i = objScenery.options.length ; i >= 0 ; i--)
	{
		objScenery.options[i] = null;
	}
	if(itemArray != null)
	{
		for(i = 0 ; i < itemArray.length; i++)
		{
			objScenery.options[i] = new Option(itemArray[i][1]);
			if(itemArray[i][0] != null)
			{
			   objScenery.options[i].value = itemArray[i][0];
			}
		}
	}
	else
	{
		objScenery.options[0] = new Option("景区/城市",0);
	}
}

