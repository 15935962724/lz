function keyPress1(e)
{
	if(navigator.appName == "Microsoft Internet Explorer")
	{
		if(window.event.keyCode == "13")
		{
			search1();
			return false;
		}
	}
	else if(navigator.appName == "Netscape")
	{
		var keyCode = new Number(e.which);
		if(keyCode == 13)
		{
			search1();
			return false;
		}
	}
}

